export function getInitialLocale(): Locale {
  const saved = window.localStorage.getItem("ficbase.locale");
  if (isLocale(saved)) return saved;

  const browserLocale = window.navigator.language;
  if (browserLocale.toLowerCase().startsWith("zh")) return "zh-CN";
  return fallbackLocale;
}

export function getNextLocale(locale: Locale): Locale {
  const index = locales.findIndex((item) => item.code === locale);
  return locales[(index + 1) % locales.length].code;
}

export function getLocaleLabel(locale: Locale) {
  return locales.find((item) => item.code === locale)?.shortLabel ?? locale;
}

export function createTranslator(locale: Locale) {
  return (key: string, params?: TranslationParams) => {
    const template = messages[locale][key] ?? messages[fallbackLocale][key] ?? key;
    if (!params) return template;
    return template.replace(/\{(\w+)\}/g, (match, name) => String(params[name] ?? match));
  };
}

function isLocale(value: string | null): value is Locale {
  return value === "zh-CN" || value === "en-US";
}
